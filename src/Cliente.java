       private void cadastrarCli() {
        String url = "jdbc:mysql://localhost:3306/Zoo";
        String usuario = "root";
        String senha = "Niczaq123!";

        try (Connection conexao = DriverManager.getConnection(url, usuario, senha)) {
            String query = "INSERT INTO cliente (nome, endereco, rg, telefone, datana) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement statement = conexao.prepareStatement(query);
            statement.setString(1, txtNome.getText());
            statement.setString(2, txtEnde.getText());
            statement.setString(3, txtRG.getText());
            statement.setString(4, txtTele.getText());
            statement.setString(5, txtData.getText());
            statement.executeUpdate();
            JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso!");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar cliente no banco de dados: " + ex.getMessage());
        }
    }
