    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Consulta consul = new Consulta();
        consul.setNome(txtNome.getText());
        consul.setCpf(txtCPF.getText());
        consul.setTelefone(txtTele.getText());
        consul.setEndereFun(txtEnde.getText());
        consul.setCargo(txtCargo.getText());

        if (txtNome.getText().isEmpty() || txtCPF.getText().isEmpty() || txtTele.getText().isEmpty() || txtEnde.getText().isEmpty() || txtCargo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha todos os campos obrigatórios.", "Campos obrigatórios não preenchidos", JOptionPane.WARNING_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Funcionário cadastrado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
          ListaConsul lista = new ListaConsul();
          lista.adicionar(consul);
        
          cadastrarFun();
        
        }
        txtNome.setText("");
        txtCPF.setText("");
        txtTele.setText("");
        txtEnde.setText("");
        txtCargo.setText("");
        
       
    }//GEN-LAST:event_jButton1ActionPerformed

