         private void cadastrarFun() {
        String url = "jdbc:mysql://localhost:3306/Zoo";
        String usuario = "root";
        String senha = "Niczaq123!";

        try (Connection conexao = DriverManager.getConnection(url, usuario, senha)) {
            String query = "INSERT INTO funcionario (nome, endereco,cpf, telefone, cargo) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement statement = conexao.prepareStatement(query);
            statement.setString(1, txtNome.getText());
            statement.setString(2, txtEnde.getText());
            statement.setString(3,txtCPF.getText());
            statement.setString(4, txtTele.getText());
            statement.setString(5, txtCargo.getText());
            statement.executeUpdate();
            JOptionPane.showMessageDialog(null, "Funcionário cadastrado com sucesso!");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar funcionário no banco de dados: " + ex.getMessage());
        }
    }
